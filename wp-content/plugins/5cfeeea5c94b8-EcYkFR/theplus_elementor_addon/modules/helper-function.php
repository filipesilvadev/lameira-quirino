// Get Elementor Template 
function theplus_get_templates() {
    $templates = Theplus_Element_Load::elementor()->templates_manager->get_source( 'local' )->get_items();
    $types     = [];

    if ( empty( $templates ) ) {
        $options = [ '0' => __( 'You Havenâ€™t Saved Templates Yet.', 'theplus' ) ];
    } else {
        $options = [ '0' => __( 'Select Template', 'theplus' ) ];
        
        foreach ( $templates as $template ) {
            $options[ $template['template_id'] ] = $template['title'] . ' (' . $template['type'] . ')';
            $types[ $template['template_id'] ] = $template['type'];
        }
    }

    return $options;
}


//Get Page Ids
function theplus_get_pages_list() {
   $page_ids=get_all_page_ids();
    if ( empty( $page_ids ) ) {
        $options = [ '0' => __( 'Not Page List', 'theplus' ) ];
    } else {
        $options = [ '0' => __( 'Select Page', 'theplus' ) ];
        
        foreach ( $page_ids as $page ) {
            $options[ $page ] = get_the_title($page);
        }
    }

    return $options;
}
